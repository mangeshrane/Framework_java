package pojo;

public class Customer {
	
	protected String firstName;
	protected String lastName;
	protected String mobileNumber;
	protected String email;
	protected String password;
	protected String country;
	protected String address1;
	protected String address2;
	protected String Status;
	protected String emailNewsLetterSubscriber;
	
	public String getFirstName() {
		return firstName;
	}
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	public String getLastName() {
		return lastName;
	}
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	public String getMobileNumber() {
		return mobileNumber;
	}
	public void setMobileNumber(String mobileNumber) {
		this.mobileNumber = mobileNumber;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
	public String getAddress1() {
		return address1;
	}
	public void setAddress1(String address1) {
		this.address1 = address1;
	}
	public String getAddress2() {
		return address2;
	}
	public void setAddress2(String address2) {
		this.address2 = address2;
	}
	public String getStatus() {
		return Status;
	}
	public void setStatus(String status) {
		this.Status = status;
	}
	public String getEmailNewsLetterSubscriber() {
		return emailNewsLetterSubscriber;
	}
	public void setEmailNewsLetterSubscriber(String emailNewsLetterSubscriber) {
		this.emailNewsLetterSubscriber = emailNewsLetterSubscriber;
	}
	
	
}
